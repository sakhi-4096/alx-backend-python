#!/usr/bin/env python3

"""
Basic Annotations Example: Computing the floor of a float.
"""

import math


def floor(n: float) -> int:
    """
    Computes the floor of a given float number.

    Parameters:
        float_number (float): The float for which the floor value needs to be computed.
    Returns:
        int: The floor value of the input float.
    """
    return math.floor(n)
