#!/usr/bin/env python3

"""
    Simple module showcasing basic variable annotations for string concatenation.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the result.

    Parameters:
        first_string (str): The first string to be concatenated.
        second_string (str): The second string to be concatenated.
    Returns:
        str: The result of concatenating the two input strings.
    """
    return str1 + str2
